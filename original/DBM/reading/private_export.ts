async function main() {
    const privateAddress = "0x8377Ff4AEd639CBd75223aa9a9caB257864591Ca";
    const [deployer] = await ethers.getSigners();
    let _private: PlatformVesting;

    _private = await (await ethers.getContractFactory("PlatformVesting")).attach(privateAddress);

    let outputs = [];

    let count = 0;
    for(let wallet of wallets) {
        console.log(wallet);
        let data = await _private.userPropertiesList(wallet);
        outputs.push({wallet: wallet, data: data})

        if(count > 0 && count % 10 == 0) {
            console.log("wait...")
            await sleep(20000);
        }
        await sleep(2000);
        count++;
    }

    let vestings = await _private.vestingPropertiesList();
    let s = "";

    for(let output of outputs)  {
        let vesting = vestings[output.data.vestingId.toNumber()];
        s += (output.data.isActive)
            ? ("true," + output.wallet + "," + output.data.spentAmount + "," + vesting.amountForUser + "\n")
            : ("false," + output.wallet + ",0,0\n");
    }

    fs.writeFileSync('./private_backup.csv', s);

    console.log("done");
}

